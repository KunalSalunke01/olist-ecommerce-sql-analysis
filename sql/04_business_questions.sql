ORDER BY 1;

-- Find the top 3 sellers within each state.
SELECT * FROM (
SELECT seller_state , s.seller_id , RANK() OVER(PARTITION BY seller_state ORDER BY SUM(price) DESC) seller_rank
FROM sellers s
INNER JOIN order_items i
ON s.seller_id = i.seller_id
GROUP BY seller_state ,s.seller_id
ORDER BY 1,3)
WHERE seller_rank <=3;

-- Rank products within each category based on sales.
SELECT product_category_name , p.product_id , RANK() OVER(PARTITION BY product_category_name ORDER BY COUNT(i.product_id) DESC) FROM products p
INNER JOIN order_items i
ON p.product_id = i.product_id
GROUP BY product_category_name,p.product_id
ORDER BY 1,3;

-- Which sellers sell products in multiple categories?
SELECT i.seller_id , COUNT(DISTINCT product_category_name) FROM order_items i
INNER JOIN products p
ON i.product_id = p.product_id
GROUP BY seller_id
HAVING COUNT(DISTINCT product_category_name) >1
ORDER BY 2 DESC;

-- Find the average product price by category.
SELECT product_category_name_english , ROUND(AVG(price),2) cate_avg FROM products p
INNER JOIN order_items i
ON p.product_id = i.product_id
INNER JOIN product_category_translation t
ON p.product_category_name = t.product_category_name
GROUP BY product_category_name_english
ORDER BY 2 DESC;

-- Find categories having more than 1,000 products sold.
SELECT product_category_name_english , COUNT(i.product_id) prod_sold FROM products p
INNER JOIN order_items i
ON p.product_id = i.product_id
INNER JOIN product_category_translation t
ON p.product_category_name = t.product_category_name
GROUP BY product_category_name_english
