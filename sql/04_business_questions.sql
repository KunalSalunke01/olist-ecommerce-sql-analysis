-- Calculate the average delivery time for each seller.
SELECT 
	seller_id ,
	EXTRACT(DAY FROM AVG(order_delivered_customer_date-order_purchase_timestamp )) AS days 
FROM order_items i
INNER JOIN orders o
ON o.order_id = i.order_id
WHERE order_delivered_customer_date IS NOT NULL
GROUP BY seller_id
ORDER BY 2;

-- Find sellers with the fastest average delivery time.
SELECT 
	seller_id ,
	EXTRACT(DAY FROM AVG(order_delivered_customer_date-order_purchase_timestamp )) AS days 
FROM order_items i
INNER JOIN orders o
ON o.order_id = i.order_id
WHERE order_delivered_customer_date IS NOT NULL
GROUP BY seller_id
ORDER BY 2
LIMIT 10;

-- Calculate the percentage of late deliveries by state.
WITH late_order AS(
SELECT customer_state ,COUNT(*) AS late_delivered FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
WHERE order_delivered_customer_date > order_estimated_delivery_date
GROUP BY customer_state
),
total_order AS(
SELECT customer_state , COUNT(DISTINCT order_id) AS total_delivered FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
GROUP BY customer_state
)
SELECT  
	l.customer_state , 
	ROUND( 100.0*late_delivered/total_delivered ,2) AS late_delivery_percent
FROM late_order l
INNER JOIN total_order t
ON l.customer_state = t.customer_state
ORDER BY 2 DESC;



