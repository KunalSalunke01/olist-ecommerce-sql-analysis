-- Find customers who placed more than five orders.
SELECT customer_unique_id , COUNT(order_id) AS orders FROM orders o
INNER JOIN customers c
ON c.customer_id = o.customer_id
GROUP BY customer_unique_id
HAVING COUNT(order_id) > 5
ORDER BY 2 DESC;

-- Find customers who never placed an order.
SELECT customer_unique_id FROM customers c
WHERE customer_id NOT IN (SELECT customer_id FROM orders);

-- Find customers who purchased from more than one seller.
SELECT 
	customer_unique_id,
	COUNT(DISTINCT seller_id) purchase_seller_cnt 
FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
INNER JOIN order_items i
ON o.order_id = i.order_id
GROUP BY 1
HAVING COUNT(DISTINCT seller_id) >1
ORDER BY 2 DESC;

-- Find customers who spent more than the average customer.

WITH cust_spends AS (
    SELECT customer_unique_id, SUM(payment_value) AS spending
    FROM customers c
    INNER JOIN orders o ON o.customer_id = c.customer_id
    INNER JOIN order_payments p ON o.order_id = p.order_id
    GROUP BY customer_unique_id
)
SELECT customer_unique_id, spending
FROM cust_spends
WHERE spending > (SELECT AVG(spending) FROM cust_spends)
ORDER BY spending DESC;

-- Find the top 20 customers by total spending.
SELECT customer_unique_id, SUM(payment_value) AS total_spending FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
INNER JOIN order_payments p
ON o.order_id = p.order_id
GROUP BY customer_unique_id
ORDER BY 2 DESC
LIMIT 20;

-- Calculate the average spending per customer by state.
WITH cust_spends AS (
SELECT customer_unique_id,customer_state, SUM(payment_value) AS spending FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
INNER JOIN order_payments p
ON o.order_id = p.order_id
GROUP BY customer_unique_id , customer_state
)
SELECT customer_state, ROUND(AVG(spending),2) AS avg_spending FROM cust_spends
GROUP BY customer_state
ORDER BY 2 DESC;

-- Find customers who have only one order.
SELECT customer_unique_id, COUNT(order_id) AS order_cnt FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
GROUP BY customer_unique_id
HAVING COUNT(order_id) = 1;

-- Rank customers based on total spending.
SELECT 
	RANK() OVER(ORDER BY  SUM(payment_value) DESC), 
	customer_unique_id, 
	SUM(payment_value) AS total_spending 
FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
INNER JOIN order_payments p
ON o.order_id = p.order_id
GROUP BY customer_unique_id
ORDER BY 1;

-- Find customers who purchased products from multiple categories.
SELECT customer_unique_id, COUNT(DISTINCT product_category_name) AS cate_cnt FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
INNER JOIN order_items i
ON o.order_id = i.order_id
INNER JOIN products p
ON i.product_id = p.product_id
GROUP BY customer_unique_id
HAVING COUNT(DISTINCT product_category_name) >1 
ORDER BY 2 DESC; 

-- Find repeat customers who ordered in multiple years.
SELECT customer_unique_id, COUNT(DISTINCT EXTRACT(YEAR FROM order_purchase_timestamp)) AS avg_spending FROM customers c
INNER JOIN orders o
ON o.customer_id = c.customer_id
GROUP BY customer_unique_id
HAVING COUNT(DISTINCT  EXTRACT(YEAR FROM order_purchase_timestamp)) > 1
ORDER BY 2 DESC;

